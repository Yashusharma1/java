package _04_STRING_AND_PRINTING;

public class PRINTING {
    public static void main(String[] args) {
        /*
        * THERE ARE FOUR PRINTING METHOD
        *
        * System.out.println(); THIS IS USED TO PRINT THE OUTPUT WITH THE NEW LINE
        * System.out.print();   THIS IS USED TO PRINT THE OUTPUT WITHOUT THE NEW LINE
        * System.out.printf();
        * System.out.format();
        *
        * */
        int x= 12;
        int y=12;
        System.out.println("THE SUM OF X AND Y IS: "+(x+y) );
        System.out.print("THE SUM OF THE X AND Y IS :"+ (x+y));


    }
}


