package _02_DATA_TYPES;

public class RANGES {
    public static void main(String[] args)
    {
        //IN THIS WE WILL FIND THE RANGE OF THE DATA TYPES
        System.out.println("THE MAX VALUE OF THE INTEGER IS: "+Integer.MAX_VALUE);
        System.out.println("THE MIN VALUE OF THE INTEGER IS: "+Integer.MIN_VALUE);
        System.out.println("THE SIZE VALUE OF THE INTEGER IS: "+Integer.SIZE);
        System.out.println("THE NUMBER OF BYTES IN THE  INTEGER IS: "+Integer.BYTES);

        // SAME GOES FOR THE Byte,Short ,Int ,long,double and charcter
        // boolean does not have the max and min value because it has only true and false'
    }

}








