package _02_DATA_TYPES;

public class VARIABLE
{
    public static void main(String[] args)
    {
        // VARIABLE ARE USED FOR STORING THE VALUE
        int a = 5;
        // IN THISc  A IS THE VARIABLE WHOSE DATA TYPE IS INT AND IT IS DECLARED

        System.out.println(a);

        /*
        * THERE ARE RULES FOR NAMING A VARIABLE
        * NAME CAN CONTAIN : LETTER , NUMBER ,$ ,_
        * CAN START WITH : LETTER $ , _
        * IT IS CASE SENSITIVE
        * NAME CANNOT BE A KEYWORD AND CLASS NAME
        * */
    }
}
