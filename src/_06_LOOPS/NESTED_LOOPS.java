package _06_LOOPS;

public class NESTED_LOOPS {
    public static void main(String[] args) {
        // NESTED LOOPS

        for(int i = 1;i<=5;i++){
            for (int e = 1;e<=5;e++){
                System.out.print(i + "," + e);
            }
            System.out.println("");
        }
    }
}
