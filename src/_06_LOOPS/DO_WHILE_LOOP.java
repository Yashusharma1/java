package _06_LOOPS;

public class DO_WHILE_LOOP {
    public static void main(String[] args) {
        // TABLE OF TWO USING DO WHILE LOOP
        // IN THIS LOOP IS EXECUTED ONE TIME
        int i =1;
        do {
            i= i*2;
            System.out.println("THE TABLE OF TWO IS :"+i);
        } while (i<100);
    }
}
